# backend/explain/stage4/ollama_adapter.py
"""
Ollama Adapter for Stage 4
==========================

Formats explanation text from Stage 3 into natural Vietnamese using Ollama.

This is a FORMATTER layer, not a reasoning layer:
  - Does NOT add new information
  - Does NOT change meaning
  - Does NOT infer anything
  - Has mandatory fallback

Pipeline position:
    Inference → XAI (GĐ2) → Rule+Template (GĐ3) → Ollama (GĐ4) → API → Frontend
"""

