_stage4_engine: Optional[Stage4Engine] = None


def get_stage4_engine() -> Stage4Engine:
    """Get or create singleton Stage4Engine."""
    global _stage4_engine
    if _stage4_engine is None:
        _stage4_engine = Stage4Engine()
        _stage4_engine.load_config_file()
    return _stage4_engine


def format_with_llm(stage3_output: Dict[str, Any]) -> Dict[str, Any]:
    """
    Format Stage 3 output using Ollama (main entry point).
    
    Args:
        stage3_output: Output from Stage 3
        
    Returns:
        Stage 4 output dict
        
    Example:
        >>> stage3_out = {
        ...     "trace_id": "abc123",
        ...     "career": "Data Scientist",
        ...     "reasons": ["Điểm Toán vượt ngưỡng (shap)"],
        ...     "explain_text": "Bạn phù hợp với Data Scientist vì:...",
        ...     "confidence": 0.87
        ... }
        >>> result = format_with_llm(stage3_out)
        >>> print(result["llm_text"])
    """
    engine = get_stage4_engine()
    result = engine.format(stage3_output)
    return result.to_dict()


