_ANALYTICAL_PROMPT_PATH = Path(__file__).parent / "prompts" / "analytical_explanation.txt"

_analytical_template: Optional[str] = None


def _load_analytical_template() -> str:
    """Load (and cache) the analytical_explanation.txt prompt template."""
    global _analytical_template
    if _analytical_template is None:
        if _ANALYTICAL_PROMPT_PATH.exists():
            _analytical_template = _ANALYTICAL_PROMPT_PATH.read_text(encoding="utf-8")
        else:
            logger.warning("analytical_explanation.txt not found at %s", _ANALYTICAL_PROMPT_PATH)
            _analytical_template = ""
    return _analytical_template


def _split_system_user(template: str) -> tuple[str, str]:
    """Extract (system_prompt, user_prompt) from a SYSTEM:/USER: template."""
    system_prompt = ""
    user_prompt = template
    if "SYSTEM:" in template and "USER:" in template:
        parts = template.split("USER:", 1)
        system_part = parts[0].replace("SYSTEM:", "", 1).strip()
        system_prompt = system_part
        user_prompt = parts[1].strip()
    return system_prompt, user_prompt


def generate_analytical_summary(
    profile_json_str: str,
    timeout: float = 20.0,
) -> str:
    """
    Generate a deep analytical explanation from a serialised profile JSON string.

    Uses the ``analytical_explanation.txt`` prompt template which instructs the
    LLM to reason causally about skill alignment, education relevance, ability
    implications, strength-risk tradeoff, and development trajectory.

    This function is **synchronous** so it can be run via
    ``asyncio.get_event_loop().run_in_executor(None, generate_analytical_summary, ...)``
    from async callers.

    Parameters
    ----------
    profile_json_str:
        JSON-serialised dict containing profile fields (skills, interests,
        education_level, ability_score, goals, top_career, score_breakdown, …).
    timeout:
        Per-request HTTP timeout forwarded to the underlying Ollama client.
        Caller should set this to a budget that fits within the outer pipeline
        timeout (default 20 s).

    Returns
    -------
    str
        Generated analytical text, or ``""`` on any failure (circuit-breaker
        open, timeout, empty response).  The caller is responsible for applying
        its own fallback when an empty string is returned.
    """
    template = _load_analytical_template()
    if not template:
        logger.warning("Analytical explanation template missing — skipping LLM call")
        return ""

    filled = template.replace("{{profile_json}}", profile_json_str)
    system_prompt, user_prompt = _split_system_user(filled)

    client = get_ollama_client()

    # Save and restore client timeout so we don't permanently mutate the singleton
    _orig_timeout = client._timeout
    try:
        client.configure(timeout=max(5.0, timeout * 0.9))
        response = client.generate(
            prompt=user_prompt,
            system_prompt=system_prompt or None,
            temperature=0.4,
            max_tokens=600,
        )
    except Exception as exc:
        logger.warning("generate_analytical_summary: LLM call raised %s: %s", type(exc).__name__, exc)
        return ""
    finally:
        try:
            client.configure(timeout=_orig_timeout)
        except Exception:
            pass

    if response.success and response.text and len(response.text.strip()) >= 10:
        return response.text.strip()

    logger.debug(
        "generate_analytical_summary: non-success response (error=%r, text_len=%d)",
        response.error,
        len(response.text),
    )
    return ""
